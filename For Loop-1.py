# For loop is used to iterate over each character in a string and then do something with it.
for item in 'Python':
    print(item)

# List case
for item in ['John', 'Harry', 'Rupak']:
    print(item)


# List case - Numbers
for item in [1,2,3,4,5]:
    print(item)

# Range case - 3 to 9
for item in range(3, 10):
    print(item)

# Range case - 3 to 9 at 2 intervals
for item in range(3, 10, 2):
    print(item)

