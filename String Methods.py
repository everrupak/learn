# String Methods
# print and len are functions
# upper, lower, find, replace are methods
course = 'Python for Beginners'
print(len(course))
print(course.upper())
print(course.lower())
print(course.find('n'))
print(course.replace('Beginners', 'Experts'))
print(course.replace('n', 'N'))
print('Python' in course)
